RVTEST_RV64U
RVTEST_CODE_BEGIN

  li t0, 1000000

1:
  la t1, tdat

  lw t2, 0(t1)
  xori t3, t2, 1234
  sll t4, t2, 7
  sub t5, t3, t2
  sw t5, 0(t1)

  add t0, t0, -1
  bnez t0, 1b

RVTEST_PASS

RVTEST_CODE_END

  .data
